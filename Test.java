public class Test{
	
	public static void main(String[] args) {
		int A = 10;
		int B = 10;
		int C = 20;
		int D = 5;
		
		System.out.println("A - B = " + (A - B));
		System.out.println("A + B = " + (A + B));
		System.out.println("A * B - C = " + (A * B - C));
		System.out.println("(A + B) / C = " + ((A + B) / C));
		System.out.println("A --  = " + (A-- ));
		System.out.println("A ++  = " + (A++ ));
		System.out.println("(C + D) % B = " + ((C + D) % B  ));
		System.out.println("++A  = " + (++A ));
		System.out.println("--A  = " + (--A ));
	}
	
}
